// Test execution function
function runTest(suite) {
  console.log(`üß™ Running: ${suite.name}`);
  console.log(`   Description: ${suite.description}`);
  
  const startTime = Date.now();
  
  try {
    const output = execSync(suite.command, {
      cwd: __dirname,
      encoding: 'utf8',
      timeout: config.timeout,
      stdio: 'pipe'
    });
    
    const duration = Date.now() - startTime;
    console.log(`   ‚úÖ PASSED (${duration}ms)\n`);
    
    results.passed++;
    results.details.push({
      name: suite.name,
      status: 'PASSED',
      duration,
      output: output.substring(0, 500) // Truncate long output
    });
    
  } catch (error) {
    const duration = Date.now() - startTime;
    console.log(`   ${suite.required ? '‚ùå' : '‚ö†Ô∏è'} ${suite.required ? 'FAILED' : 'SKIPPED'} (${duration}ms)`);
    if (suite.required) {
      console.log(`   Error: ${error.message.substring(0, 200)}...\n`);
    } else {
      console.log(`   Note: ${suite.name} is optional and may not exist yet\n`);
    }
    
    results.failed++;
    results.details.push({
      name: suite.name,
      status: suite.required ? 'FAILED' : 'SKIPPED',
      duration,
      error: error.message.substring(0, 500),
      required: suite.required
    });
  }
}

