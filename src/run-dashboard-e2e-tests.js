console.log('========================');
console.log(`Total Tests: ${results.total}`);
console.log(`Passed: ${results.passed} ‚úÖ`);
console.log(`Failed: ${results.failed} ${results.failed > 0 ? '‚ùå' : '‚úÖ'}`);
